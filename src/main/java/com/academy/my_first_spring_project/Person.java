package com.academy.my_first_spring_project;

public class Person {
    private Long id;
    private String name;

    public Long getId(){
        return this.id;
    }
    public String getName(){
        return name;
    }
    public void setId(Long id ){
        this.id = id;
    }
    public void setName(String name){
        this.name = name;
    }
}




